package com.mycompany.servidorp1r.Classes;

/**
 *
 * @author waliray
 */
public class Puerto {

    private String ip;
    private String mac;
    private String estado;

    public Puerto(String ip, String mac, String estado) {
        this.ip = ip;
        this.mac = mac;
        this.estado = estado;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

}
